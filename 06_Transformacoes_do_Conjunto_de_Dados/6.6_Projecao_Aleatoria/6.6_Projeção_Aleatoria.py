########## 6.6. Projeção aleatória ##########



    # O módulo sklearn.random_projection implementa uma maneira simples e computacionalmente eficiente de reduzir a dimensionalidade dos dados trocando uma quantidade controlada de precisão (como variação adicional) para tempos de processamento mais rápidos e tamanhos de modelo menores. Este módulo implementa dois tipos de matriz aleatória não estruturada: matriz aleatória gaussiana e matriz aleatória esparsa.

    # As dimensões e distribuição das matrizes de projeções aleatórias são controladas de modo a preservar as distâncias pareadas entre quaisquer duas amostras do conjunto de dados. Assim, a projeção aleatória é uma técnica de aproximação adequada para o método baseado em distância. 





