##########  6.2. Extração de recursos ##########


    # O módulo sklearn.feature_extraction pode ser usado para extrair recursos em um formato compatível com algoritmos de aprendizado de máquina de conjuntos de dados que consistem em formatos como texto e imagem.

    # Nota: A extração de recursos é muito diferente da seleção de recursos: a primeira consiste em transformar dados arbitrários, como texto ou imagens, em recursos numéricos utilizáveis para aprendizado de máquina. O último é uma técnica de aprendizado de máquina aplicada a esses recursos. 