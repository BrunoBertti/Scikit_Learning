########## 6.4.1. Imputação univariada vs. multivariada ##########



    # Um tipo de algoritmo de imputação é univariado, que imputa valores na i-ésima dimensão de recurso usando apenas valores não omissos nessa dimensão de recurso (por exemplo, impute.SimpleImputer). Por outro lado, os algoritmos de imputação multivariada usam todo o conjunto de dimensões de recursos disponíveis para estimar os valores ausentes (por exemplo, impute.IterativeImputer). 