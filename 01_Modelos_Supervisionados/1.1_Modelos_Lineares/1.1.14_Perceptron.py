########## 1.1.14 Perceptron ##########

    # O Perceptron é outro algoritmo de classificação simples adequado para aprendizagem em larga escala. Por padrão:

        # Não requer taxa de aprendizado.

        # Não é regularizado (penalizado).

        # Atualiza seu modelo apenas em caso de erros.

    # A última característica implica que o Perceptron é um pouco mais rápido de treinar do que o SGD com a perda de articulação e que os modelos resultantes são mais esparsos.